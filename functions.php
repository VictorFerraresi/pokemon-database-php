            case 3: return 'Water';     
            case 4: return 'Flying';
            case 5: return 'Grass';
            case 6: return 'Poison';
            case 7: return 'Eletric';
            case 8: return 'Ground';    
            case 9: return 'Psychic';
            case 10: return 'Rock';
            case 11: return 'Ice';
            case 12: return 'Bug';
            case 13: return 'Dragon';   
            case 14: return 'Ghost';
            case 15: return 'Dark';
            case 16: return 'Steel';
            case 17: return 'Fairy';
        }
    }
